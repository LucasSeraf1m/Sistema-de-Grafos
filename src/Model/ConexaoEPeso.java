
package Model;

public class ConexaoEPeso {
    private int noOrigem;
    private int noDestino;
    private int peso;

    public int getNoOrigem() {
        return noOrigem;
    }

    public void setNoOrigem(int noOrigem) {
        this.noOrigem = noOrigem;
    }

    public int getNoDestino() {
        return noDestino;
    }

    public void setNoDestino(int noDestino) {
        this.noDestino = noDestino;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }
}
